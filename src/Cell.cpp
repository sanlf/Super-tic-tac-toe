#include "../include/Cell.h"

/**************************************************************************************************/
Cell::Cell()
{
    p0 = Point(0, 0);
    p1 = Point(0, 0);
    piece = "";
}
/**************************************************************************************************/
Cell::Cell(Point p0, Point p1, std::string piece/* = ""*/)
{
    this->p0 = p0;
    this->p1 = p1;
    this->piece = piece;
}
/**************************************************************************************************/
void Cell::draw(ALLEGRO_FONT* font) const
{
    al_draw_text(font, COLOR.WHITE, p0.x, p0.y, ALLEGRO_ALIGN_INTEGER, piece.c_str());
}
